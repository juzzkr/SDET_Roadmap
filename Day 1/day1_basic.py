# Variables
name = "Kunal"
age = 24
print(f"My name is {name} and I am {age} years old")

# Lists
numbers = [1, 2, 3, 4, 5]
print("Numbers:", numbers)

# Dictionary
student = {"name": "Kunal", "marks": 85}
print(student["name"], "scored", student["marks"])

# Loops
for i in range(5):
    print("Loop:", i)

# If/else
num = 7
if num % 2 == 0:
    print("Even")
else:
    print("Odd")

# Functions
def greet(person):
    return f"Hello, {person}!"

print(greet("Kunal"))
